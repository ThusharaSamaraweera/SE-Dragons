package SEgragons.JuniorHack;

public class Contact {
    String name;
    String phoneNo;

    public void setName(String name){
        this.name = name;
    }
    public void setPhoneNo(String phoneNo){
        this.phoneNo = phoneNo;
    }
    public void getName(){
        this.name = name;
    }
    public void getPhoneNo(){
        this.phoneNo = phoneNo;
    }
}
